package ch02.sec03;

public class CharExample {

	public static void main(String[] args) {
		char C1 = 'A'; // 문자 저장
		char C2 = 65;
		char C3 = '가';
		char C4 = 44032;
		
		System.out.println(C1);
		System.out.println(C2);
		System.out.println(C3);
		System.out.println(C4);
		
		//char c5 = ''; // 컴파일 에러
		char C6 = ' ';//공백으로 초기화
		System.out.println(C6);
	}

}
